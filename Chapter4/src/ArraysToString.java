import java.util.Arrays;

public class ArraysToString {
    public static void main(String[] args) {
        String[] stringArray = {
                "string 1",
                "string 2",
                "string 3",
                "string 4",
                "string 5",
        };

        System.out.println(stringArray);
        System.out.println(Arrays.toString(stringArray));
    }
}
